package model;

public class Fila {
	
	int idFila;
	String nomeFila;
	
	
	public Fila(int idFila, String nomeFila) {
		this.idFila = idFila;
		this.nomeFila = nomeFila;
	}


	public int getIdFila() {
		return idFila;
	}


	public void setIdFila(int idFila) {
		this.idFila = idFila;
	}


	public String getNomeFila() {
		return nomeFila;
	}


	public void setNomeFila(String nomeFila) {
		this.nomeFila = nomeFila;
	}
	
	
}
